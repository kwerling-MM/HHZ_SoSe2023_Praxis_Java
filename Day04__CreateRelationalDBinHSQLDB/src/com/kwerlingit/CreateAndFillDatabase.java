    private void createDB(Connection conn) throws SQLException {
        List<String> createCmds = getCreateCmds();

        runCmds( conn, createCmds);
    }

    private void runCmds(Connection conn, List<String> commandList) throws SQLException {
        try( Statement stmt = conn.createStatement() ) {
            for (String cmd : commandList) {
                stmt.executeUpdate(cmd);
            }
        }
    }

    private List<String> getCreateCmds() {
        List<String> createTables = new ArrayList<String>();

        /*==============================================================*/
        /* Drop Tables first                                            */
        /*==============================================================*/
        createTables.add("DROP TABLE  Customers     IF EXISTS  CASCADE");
        createTables.add("DROP TABLE  Orders        IF EXISTS  CASCADE");
        createTables.add("DROP TABLE  OrderItems    IF EXISTS  CASCADE");
        createTables.add("DROP TABLE  Products      IF EXISTS  CASCADE");
        createTables.add("DROP TABLE  Suppliers     IF EXISTS  CASCADE");

        /*==============================================================*/
        /* Table: Customer                                              */
        /*==============================================================*/
        createTables.add( "create table Customers ("
                + "   Id                   int                  ,"
                + "   FirstName            nvarchar(40)         not null,"
                + "   LastName             nvarchar(40)         not null,"
                + "   City                 nvarchar(40)             null,"
                + "   Country              nvarchar(40)             null,"
                + "   Phone                nvarchar(20)             null,"
                + "   constraint PK_CUSTOMER primary key (Id)"
                + ")"
                + ";");

        /*==============================================================*/
        /* Table: "Order"                                               */
        /*==============================================================*/
        createTables.add( "create table Orders ("
                + "   Id                   int                  ,"
                + "   OrderDate            datetime             default CURRENT_TIMESTAMP    not null,"
                + "   OrderNumber          nvarchar(10)                                          null,"
                + "   CustomerId           int                                               not null,"
                + "   TotalAmount          decimal(12,2)        default 0.0                      null,"
                + "   constraint PK_ORDER primary key (Id)"
                + ")"
                + ";");


        /*==============================================================*/
        /* Table: OrderItem                                             */
        /*==============================================================*/
        createTables.add( "create table OrderItems ("
                + "   Id                   int                  ,"
                + "   OrderId              int                                not null,"
                + "   ProductId            int                                not null,"
                + "   UnitPrice            decimal(12,2)        default 0.0   not null,"
                + "   Quantity             int                  default 1     not null,"
                + "   constraint PK_ORDERITEM primary key (Id)"
                + ")"
                + ";");

        /*==============================================================*/
        /* Table: Product                                               */
        /*==============================================================*/
        createTables.add( "create table Products ("
                + "   Id                   int                                         ,"
                + "   ProductName          nvarchar(50)                        not null,"
                + "   SupplierId           int                                 not null,"
                + "   UnitPrice            decimal(12,2)        default 0.0        null,"
                + "   Package              nvarchar(30)                            null,"
                + "   IsDiscontinued       bit                  default 0     not null ,"
                + "   constraint PK_PRODUCT primary key (Id)"
                + ")"
                + ";");

        /*==============================================================*/
        /* Table: Supplier                                              */
        /*==============================================================*/
        createTables.add( "create table Suppliers ("
                + "   Id                   int                          ,"
                + "   CompanyName          nvarchar(40)         not null,"
                + "   ContactName          nvarchar(50)             null,"
                + "   ContactTitle         nvarchar(40)             null,"
                + "   City                 nvarchar(40)             null,"
                + "   Country              nvarchar(40)             null,"
                + "   Phone                nvarchar(30)             null,"
                + "   Fax                  nvarchar(30)             null,"
                + "   constraint PK_SUPPLIER primary key (Id)"
                + ")"
                + ";");


        createTables.add( "alter table Orders"
                + "   add constraint FK_ORDER_REFERENCE_CUSTOMER foreign key (CustomerId)"
                + "      references Customers (Id)"
                + ";");


        createTables.add( "alter table OrderItems"
                + "   add constraint FK_ORDERITE_REFERENCE_ORDER foreign key (OrderId)"
                + "      references Orders (Id)"
                + ";");

        createTables.add( "alter table OrderItems"
                + "   add constraint FK_ORDERITE_REFERENCE_PRODUCT foreign key (ProductId)"
                + "      references Products (Id)"
                + ";");

        createTables.add( "alter table Products"
                + "   add constraint FK_PRODUCT_REFERENCE_SUPPLIER foreign key (SupplierId)"
                + "      references Suppliers (Id)"
                + ";");

        return createTables;
    }

