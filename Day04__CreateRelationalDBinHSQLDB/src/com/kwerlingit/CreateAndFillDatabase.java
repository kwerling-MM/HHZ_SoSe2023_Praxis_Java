    private class Order {
        int Id;
        java.util.Date OrderDate;
        int CustomerId;
        double TotalAmount;
        String OrderNumber;

        static SimpleDateFormat orderDateFormatter = new SimpleDateFormat("MMM d yyyy hh:mm:SSSa",Locale.ENGLISH);

        Order( int Id, String OrderDate,int CustomerId, double TotalAmount,String OrderNumber ) throws ParseException {
            this.Id = Id;
            this.OrderDate = orderDateFormatter.parse(OrderDate.replace(":000", ""));
            this.CustomerId = CustomerId;
            this.TotalAmount = TotalAmount;
            this.OrderNumber = OrderNumber;
        }
    }

