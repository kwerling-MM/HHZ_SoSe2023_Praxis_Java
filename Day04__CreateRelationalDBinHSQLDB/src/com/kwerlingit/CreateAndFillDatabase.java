    private void fillOrderItemTable(Connection conn, List<OrderItem> orderItemData) throws SQLException {
        String stm = "INSERT INTO OrderItems (Id,OrderId,ProductId,UnitPrice,Quantity)VALUES(?,?,?,?,?);";

        try( PreparedStatement preparedStatement = conn.prepareStatement(stm) ) {

            for (OrderItem orderItem : orderItemData) {
                preparedStatement.setInt(1, orderItem.Id);
                preparedStatement.setInt(2, orderItem.OrderId);
                preparedStatement.setInt(3, orderItem.ProductId);
                preparedStatement.setDouble(4, orderItem.UnitPrice);
                preparedStatement.setInt(5, orderItem.Quantity);

                preparedStatement.addBatch();
            }

            preparedStatement.executeBatch();
        }
    }

